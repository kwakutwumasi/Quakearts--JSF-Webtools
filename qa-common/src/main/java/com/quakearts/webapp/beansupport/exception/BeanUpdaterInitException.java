package com.quakearts.webapp.beansupport.exception;

public class BeanUpdaterInitException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2514344866796660121L;

	public BeanUpdaterInitException() {
		super();
	}

	public BeanUpdaterInitException(String message, Throwable cause) {
		super(message, cause);
	}

	public BeanUpdaterInitException(String message) {
		super(message);
	}

	public BeanUpdaterInitException(Throwable cause) {
		super(cause);
	}

}
