package com.quakearts.webapp.beansupport.exception;

public class BeanUpdaterException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3265872023211738374L;

	public BeanUpdaterException() {
		super();
	}

	public BeanUpdaterException(String message, Throwable cause) {
		super(message, cause);
	}

	public BeanUpdaterException(String message) {
		super(message);
	}

	public BeanUpdaterException(Throwable cause) {
		super(cause);
	}

}
