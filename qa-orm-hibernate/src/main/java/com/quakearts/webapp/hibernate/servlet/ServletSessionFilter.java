package com.quakearts.webapp.hibernate.servlet;

import java.io.IOException;

import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.FilterConfig;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;

import com.quakearts.webapp.hibernate.CurrentSessionContextHelper;

public class ServletSessionFilter implements Filter {

	@Override
	public void init(FilterConfig filterConfig) throws ServletException {
	}

	@Override
	public void doFilter(ServletRequest request, ServletResponse response, FilterChain chain)
			throws IOException, ServletException {		
		try {
			chain.doFilter(request, response);
		} finally {
			CurrentSessionContextHelper.closeOpenSessions();
		}

	}

	@Override
	public void destroy() {
	}

}
